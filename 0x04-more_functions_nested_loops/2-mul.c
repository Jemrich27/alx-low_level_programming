/**
 * mul - multiplies two numbers
 *
 * Description: Multiplies two integers and returns the result
 * @a: The first number to be multiplied
 * @b: The second number to be multiplied
 * Return: the product of two numbers
 */

int mul(int a, int b)
{
	int m = a * b;

	return (m);
}
